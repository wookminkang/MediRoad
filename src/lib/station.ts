export function normalizeLine(line: string): string {
  const l = line.replace(/\s+/g, " ").trim();
  return LINE_ALIASES[l] ?? l;
}

/** 역명에 "역" 접미 정확히 1회 보장 */
export function normalizeStationName(name: string): string {
  const n = name.trim();
  return n.endsWith("역") ? n : `${n}역`;
}

